function sanitizeCart(cartLike) {
  if (!cartLike || typeof cartLike !== "object") {
    return {};
  }

  return Object.fromEntries(
    Object.entries(cartLike).filter(
      ([id, qty]) => typeof id === "string" && Number.isFinite(qty) && qty > 0
    )
  );
}

function getOrderItemsSummary() {
  const lines = [];
  for (const [id, qty] of Object.entries(state.cart)) {
    if (!qty || qty <= 0) continue;
    const product = getProductById(id);
    if (!product) continue;
    lines.push(`${product.name} x${qty} (${formatHearts(product.price)} each)`);
  }
  return lines.join(" | ");
}

function getOrderEmailContent() {
  const lines = [];
  const linesWithImages = [];
  const htmlBlocks = [];

  for (const [id, qty] of Object.entries(state.cart)) {
    if (!qty || qty <= 0) continue;
    const product = getProductById(id);
    if (!product) continue;

    const hearts = formatHearts(product.price);
    const images = getProductImages(product);
    const firstImage = images[0] || "(no image)";
    lines.push(`${product.name} x${qty} (${hearts} each)`);
    linesWithImages.push(
      `${product.name} x${qty} | ${hearts} each | image: ${firstImage}`
    );

    const safeName = escapeHtml(product.name);
    const safeHearts = escapeHtml(hearts);
    const safeImage = escapeHtml(firstImage);
    htmlBlocks.push(
      `<div style="margin-bottom:12px;"><p><strong>${safeName}</strong> x${qty} (${safeHearts} each)</p><p>Image: ${safeImage}</p>${
        firstImage !== "(no image)"
          ? `<img src="${safeImage}" alt="${safeName}" style="max-width:220px;border-radius:8px;" />`
          : ""
      }</div>`
    );
  }

  return {
    itemsSummary: lines.join(" | "),
    itemsWithImages: linesWithImages.join("\n"),
    itemsHtml: htmlBlocks.join(""),
  };
}

function openProductDetail(productId) {
  const product = getProductById(productId);
  if (!product) return;

  const images = getProductImages(product);
  detailImageScroll.innerHTML = "";
  if (images.length > 0) {
    renderImageGallery(detailImageScroll, images, product.name, "detail-image-slide");
  } else {
    detailImageScroll.innerHTML = '<p class="empty-note">No images available.</p>';
  }

  detailDescription.textContent = product.description || "No description available.";
  state.selectedProductId = productId;
  shopHomeView.hidden = true;
  productDetailView.hidden = false;
  closeCart();
  window.scrollTo({ top: 0, behavior: "smooth" });
}

function closeProductDetail() {
  state.selectedProductId = null;
  productDetailView.hidden = true;
  shopHomeView.hidden = false;
}

function getProductById(productId) {
  return products.find((product) => product.id === productId);
}

function getProductImages(product) {
  if (Array.isArray(product.images) && product.images.length > 0) {
    return product.images;
  }
  if (product.image) {
    return [product.image];
  }
  return [];
}

function renderImageGallery(container, imageList, altText, imageClass) {
  container.innerHTML = "";
  for (const imageUrl of imageList) {
    const img = document.createElement("img");
    img.className = imageClass;
    setImageWithFallback(img, imageUrl);
    img.alt = altText;
    img.loading = "lazy";
    img.referrerPolicy = "no-referrer";
    container.append(img);
  }
}

function setImageWithFallback(imgElement, url) {
  const candidates = buildImageCandidates(url);
  let index = 0;

  const loadNext = () => {
    if (index >= candidates.length) {
      return;
    }
    imgElement.src = candidates[index];
    index += 1;
  };

  imgElement.addEventListener("error", loadNext);
  loadNext();
}

function buildImageCandidates(url) {
  const candidates = [
    url,
    url.replace("_.avif", "_.webp"),
    url.replace("_.avif", ""),
    url.replace(/\.jpg_\d+x\d+q\d+\.jpg(?:_.avif|_.webp)?$/, ".jpg"),
  ];
  return [...new Set(candidates)];
}


function startUserSession(username) {
  state.activeUser = username;
  const users = loadUsers();
  const profile = users[username] || { cart: {}, lastLoginAt: null };

  const legacyCart = loadLegacyCart();
  if (Object.keys(profile.cart || {}).length === 0 && Object.keys(legacyCart).length > 0) {
    profile.cart = legacyCart;
    localStorage.removeItem(LEGACY_CART_STORAGE_KEY);
  }

  profile.lastLoginAt = new Date().toISOString();
  state.cart = sanitizeCart(profile.cart);
  users[username] = profile;
  saveUsers(users);
}

function initShop() {
  if (shopInitialized) {
    return;
  }
  shopInitialized = true;

  renderCategoryBubbles();
  renderProducts();
  renderCart();

  searchInput.addEventListener("input", (event) => {
    state.query = event.target.value.trim().toLowerCase();
    renderProducts();
  });

  openCartBtn.addEventListener("click", openCart);
  closeCartBtn.addEventListener("click", closeCart);
  overlay.addEventListener("click", closeCart);
  detailBackBtn.addEventListener("click", closeProductDetail);

  checkoutBtn.addEventListener("click", () => {
    if (getItemCount() === 0) {
      window.alert("Your cart is empty. Add something sweet first.");
      return;
    }
    setCheckoutStatus("");
    checkoutDialog.showModal();
  });

  checkoutForm.addEventListener("submit", async (event) => {
    event.preventDefault();
    if (!checkoutForm.checkValidity()) {
      checkoutForm.reportValidity();
      return;
    }

    if (getItemCount() === 0) {
      window.alert("Your cart is empty. Add something sweet first.");
      return;
    }

    const formValues = new FormData(checkoutForm);
    const name = String(formValues.get("name") || "").trim();
    const address = String(formValues.get("address") || "").trim();
    const paymentMethod = String(formValues.get("paymentMethod") || "").trim();
    const message = String(formValues.get("message") || "").trim();
    const orderData = getOrderEmailContent();
    const subtotal = formatHearts(calculateSubtotal());

    checkoutSendBtn.disabled = true;
    setCheckoutStatus("Sending order...");

    try {
      const response = await fetch(EMAIL_ENDPOINT, {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Accept: "application/json",
        },
        body: JSON.stringify({
          _subject: `New Mermy Shop Order from ${name}`,
          _captcha: "false",
          _template: "table",
          customer_name: name,
          address,
          payment_method: paymentMethod,
          message: message || "(no message)",
          order_items: orderData.itemsSummary,
          order_items_with_images: orderData.itemsWithImages,
          order_images_html: orderData.itemsHtml,
          shipping: "Free",
          subtotal,
          total: subtotal,
        }),
      });

      if (!response.ok) {
        throw new Error(`Email API failed: ${response.status}`);
      }

      window.alert(`Thanks ${name}! Your order was sent.`);
      state.cart = {};
      persistCart();
      checkoutForm.reset();
      checkoutDialog.close();
      closeCart();
      renderCart();
      setCheckoutStatus("");
    } catch {
      setCheckoutStatus("Could not send now. Please try again.");
    } finally {
      checkoutSendBtn.disabled = false;
    }
  });

  initLiveSupport();
}

